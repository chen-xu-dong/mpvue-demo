<template>
<view class="container">
    <view class="header">
        <h1 class="news-title">{{title}}</h1>
        <view class="auth-info">
            {{news.newssource}}({{news.newsauthor}})
        </view>
    </view>
    <view class="news-content">
        <view v-html="news.detail"></view>
    </view>
    <button class="btn" @click="backIndex">回到首页</button> 
    <h2 class="related-title" v-if="relatedNews.length">相关文章</h2>
    <view class="news-wrap">
        <news-item v-for="news of relatedNews" :news="news" :key="news.newsid"></news-item>
    </view>
    <view class="comment-btn" @click="turnToComment" >
        <img src="/static/images/comment.png" class="comment-icon" />
    </view>
</view>
</template>

<script>
import xml2json from 'xmlstring2json'
import api from '@/api/api'
import newsItem from '@/components/news-item'

const dataArr = []

export default {
  components: {
    newsItem
  },
  data () {
    return {
      id: null,
      title: '',
      news: {},
      relatedNews: []
    }
  },
  async mounted () {
    Object.assign(this.$data, this.$options.data())
    this.id = this.$route.query.id
    this.title = this.$route.query.title
    await Promise.all([
      this.getNews(),
      this.getRelatedNews()
    ])
    dataArr.push({ ...this.$data })
  },
  onUnload () {
    dataArr.pop()
    const dataNum = dataArr.length
    if (!dataNum) return
    Object.assign(this.$data, dataArr[dataNum - 1])
  },
  methods: {
    turnToComment () {
      this.$router.push({
        path: '/pages/comment',
        query: {
          id: this.id
        }
      })
    },
    backIndex(){
        wx.switchTab({
            url: '/pages/index'
        })
    },
    async getNews () {
      let { id } = this
      id = `${id.slice(0, 3)}/${id.slice(3, 6)}`
      const news = await api.getNews(id)
      if (!news) return
      const parsedNews = xml2json(news.data).rss.channel.item
      this.news = {
        newssource: parsedNews.newssource['#text'],
        detail: parsedNews.detail['#text'].replace(/<img/g, '<img width="100%"'),
        newsauthor: parsedNews.newsauthor['#text']
      }
    },
    async getRelatedNews () {
      const newslist = await api.getRelatedNews({
          query: {
              parseJson: false
          },
          id: this.id
      })
      if (!newslist) return
      const parsedNews = JSON.parse(newslist.data.replace('var tag_jsonp =', ''))
      this.relatedNews = parsedNews.slice(0, 3).map(news => {
        return {
          title: news.newstitle,
          image: news.img,
          link: `/pages/newdetail?id=${news.newsid}&title=${news.newstitle}`,
          postdate: news.postdate
        }
      })
    }
  }
}
</script>

